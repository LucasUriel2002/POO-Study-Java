

public class retangulo  {

    public int base, altura;

    public int area () {
        return (this.base * this.altura);
    }

    public int perimetro () {
        return (this.base * this.altura);
    }

    public void data () {
        System.out.println("A Área é: " +  area() + " e o  perímetro é: " + perimetro() );
    }
    
}